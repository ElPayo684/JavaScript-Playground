const num1 = '20',
      num2 = '20.2',
      num3 = 'One',
      num4 = 20;

console.log(num1); // String
console.log(Number.parseInt(num1)); // Numero 
console.log(num2); // String
console.log(Number.parseFloat(num2)); // Numero 
console.log(num3); // String
console.log(Number.parseFloat(num3)); // NaN
console.log(Number.isInteger(num4)); // Booleano que indica si es un numero 
console.log(Number.isInteger(num1)); // Booleano que indica si es un numero 